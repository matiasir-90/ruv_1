package com.ruv.model;

public class PaisModel {

	
	private int pais_id;
	private String paisDescripcion;
	
	
	public int getPais_id() {
		return pais_id;
	}
	public void setPais_id(int id) {
		this.pais_id = id;
	}
	public String getPaisDescripcion() {
		return paisDescripcion;
	}
	public void setPaisDescripcion(String pais_desce) {
		this.paisDescripcion = pais_desce;
	}
}
